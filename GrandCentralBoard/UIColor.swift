import UIKit


extension UIColor {
    static func lipstick() -> UIColor {
        return UIColor(red: 227/255, green: 50/255, blue: 92/255, alpha: 1)
    }

    static func aquaMarine() -> UIColor {
        return UIColor(red: 34/255, green: 212/255, blue: 218/255, alpha: 1)
    }

    static func almostWhite() -> UIColor {
        return UIColor(red: 248/255, green: 248/255, blue: 248/255, alpha: 1)
    }

    static func axisGray() -> UIColor {
        return UIColor(red: 151/255, green: 151/255, blue: 151/255, alpha: 1)
    }
}
